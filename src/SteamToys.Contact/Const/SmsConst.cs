    public static SmsCountryOption GetSmsCountryOption(SmsPlatform platform, string country = "")
    {
        SmsCountryOption option = null;
        switch (platform)
        {
            case SmsPlatform.SmsActivate:
                option = SMS_ACTIVATE_COUNTRIES.FirstOrDefault(c => string.IsNullOrWhiteSpace(country) ? true : c.Country == country);
                break;
            case SmsPlatform.OnlineSim:
                option = ONLINE_SIM_COUNTRIES.FirstOrDefault(c => string.IsNullOrWhiteSpace(country) ? true : c.Country == country);
                break;
            case SmsPlatform.FiveSim:
                option = FIVE_SIM_COUNTRIES.FirstOrDefault(c => string.IsNullOrWhiteSpace(country) ? true : c.Country == country);
                break;
        }

        return option;
    }

    public static List<SmsCountryOption> GetSmsCountryOptions(SmsPlatform platform)
    {
        List<SmsCountryOption> options = SMS_ACTIVATE_COUNTRIES;
        switch (platform)
        {
            case SmsPlatform.OnlineSim:
                options = ONLINE_SIM_COUNTRIES;
                break;
            case SmsPlatform.FiveSim:
                options = FIVE_SIM_COUNTRIES;
                break;
        }

        return options;
    }

    public static SmsSeviceOption GetSmsServiceOption(SmsPlatform platform, int? id = null)
    {
        SmsSeviceOption option = null;
        switch (platform)
        {
            case SmsPlatform.SmsActivate:
                option = SMS_ACTIVATE_SERVICIES.FirstOrDefault(s => id.HasValue? true : s.Id == id);
                break;
            case SmsPlatform.OnlineSim:
                option = ONLINE_SIM_SERVICIES.FirstOrDefault(s => id.HasValue ? true : s.Id == id);
                break;
            case SmsPlatform.FiveSim:
                option = FIVE_SIM_SERVICIES.FirstOrDefault(s => id.HasValue ? true : s.Id == id);
                break;
        }

        return option;
    }
